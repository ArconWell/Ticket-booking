package ru.rsreu._0204vanyukov.model;

public class UserGroups {
    int id;
    String name;

    public UserGroups(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
